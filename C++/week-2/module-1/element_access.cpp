#include<bits/stdc++.h>

using namespace std;

int main(){
   
   string s = "Hello";
   
    cout << s << endl;
    
    cout << s[0] << endl;
     
     // at function is used to access the element of the string 
    cout << s.at(0) << endl;
    cout << s.back() << endl;
    cout << s.front() << endl;
    
    //like back and front function we can also use the following functions
    cout << s[s.size() -1] << endl;
    
    
   
   
   
   
  return 0;
}